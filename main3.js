const map = new maplibregl.Map({
    container: 'map',
    style: 'https://api.maptiler.com/maps/dataviz/style.json?key=l4fTgpopGsERnO5dl5sJ',
    center: [-12.140143650680095, 18.972594621388026],
    zoom: 2
});

map.on('load', () => {
    map.loadImage(
        'icon.png', 
        // Add an image to use as a custom marker
        (error, image) => {
            if (error) throw error;
            map.addImage('custom-marker', image);

            map.addSource('places', {
                'type': 'geojson',
                'data': places
            });
           
            // Add a layer showing the places.
            map.addLayer({
                'id': 'places',
                'type': 'symbol',
                'source': 'places',
                'layout': {
                    'icon-image': 'custom-marker',
                    'icon-size': .05,
                    'icon-overlap': 'always'
                }
            }); 
        });

     // Create a popup, but don't add it to the map yet.
    const popup = new maplibregl.Popup({
        closeButton: false,
        closeOnClick: false
    });

    map.on('mouseenter', 'places', (e) => {
        // Change the cursor style as a UI indicator.
        map.getCanvas().style.cursor = 'pointer';

        const coordinates = e.features[0].geometry.coordinates.slice();
        const description = e.features[0].properties.description;

        // Populate the popup and set its coordinates
        // based on the feature found.
        popup.setLngLat(coordinates).setHTML(description).addTo(map);
    });

    map.on('mouseleave', 'places', () => {
        map.getCanvas().style.cursor = '';
        popup.remove();
    }); 

    const filterGroup = document.getElementById("filter-group");

    // Create an array to store unique seasons (summer and winter)
    const seasons = Array.from(new Set(places.features.map(feature => feature.properties.icon)));
    
    // Initialize a counter to keep track of selected seasons
    let selectedSeasonCount = seasons.length;
    
    // Iterate through each season and create a checkbox and label
    seasons.forEach(season => {
        const input = document.createElement('input');
        input.type = 'checkbox';
        input.id = season;
        input.checked = true;
        filterGroup.appendChild(input);
    
        const label = document.createElement('label');
        label.setAttribute('for', season);
        label.textContent = season;
        filterGroup.appendChild(label);
    
        input.addEventListener('change', () => {
            // Update the counter based on checkbox state
            selectedSeasonCount += input.checked ? 1 : -1;
    
            // Update the filter based on the selected seasons
            const filter = selectedSeasonCount === seasons.length ? ['has', 'icon'] : ['in', ['get', 'icon'], ...seasons.filter(s => document.getElementById(s).checked)];
            map.setFilter('places', filter);
    
            // Update the visibility of the layer
            map.setLayoutProperty('places', 'visibility', 'visible');
        });
    });
});
